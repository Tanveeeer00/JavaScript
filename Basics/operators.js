// Arithmetic operators

let user1 = 4;
let user2 = 2;
console.log(user1 + user2);
console.log(user1 - user2);
console.log(user1 * user2);
console.log(user1 / user2);
console.log(user1 ** user2);
console.log(user1 % user2);

let valueOne = 3;
valueOne++;
console.log(valueOne);

const numberOne = "2";
const numberTwo = "4";
const numberThree = numberOne + numberTwo;
console.log(numberThree);

